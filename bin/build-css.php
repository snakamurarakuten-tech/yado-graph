<?php
declare(strict_types=1);

/**
 * CSS物理結合(改修: 22ファイル直列読込 → 1ファイル)。
 * レイアウトと同じ順序で連結し public/assets/css/app.css を生成する。
 * レイアウト側は app.css が存在すればそれを1本読み、無ければ従来の個別読込に
 * フォールバックする(開発時は個別ファイルのまま作業できる)。
 *
 * 実行: php bin/build-css.php   (Dockerビルド時に自動実行)
 */

if (PHP_SAPI !== 'cli') {
    http_response_code(404);
    exit;
}

$base = dirname(__DIR__) . '/public';

// レイアウト(layouts/app.php)と同じ読込順。ファイルを増やしたらここにも追加する。
$files = [
    '/assets/css/base/variables.css',
    '/assets/css/base/reset.css',
    '/assets/css/base/typography.css',
    '/assets/css/components/layout.css',
    '/assets/css/components/brand.css',
    '/assets/css/components/topnav.css',
    '/assets/css/components/hero.css',
    '/assets/css/components/tags.css',
    '/assets/css/components/scroller.css',
    '/assets/css/components/gallery.css',
    '/assets/css/components/info.css',
    '/assets/css/components/review.css',
    '/assets/css/components/cta.css',
    '/assets/css/components/bottom-nav.css',
    '/assets/css/components/collection.css',
    '/assets/css/components/badge.css',
    '/assets/css/components/instagram.css',
    '/assets/css/components/onboarding.css',
    '/assets/css/components/card-cta.css',
    '/assets/css/components/section-tabs.css',
    '/assets/css/components/footer.css',
    '/assets/css/components/detail-extras.css',
];

$out = "/* generated by bin/build-css.php — 直接編集しないこと */\n";
$missing = 0;
foreach ($files as $f) {
    $path = $base . $f;
    if (!is_file($path)) {
        fwrite(STDERR, "missing: {$f}\n");
        $missing++;
        continue;
    }
    $out .= "\n/* ==== {$f} ==== */\n" . file_get_contents($path) . "\n";
}

file_put_contents($base . '/assets/css/app.css', $out);
echo 'app.css generated: ' . number_format(strlen($out)) . " bytes, " . (count($files) - $missing) . " files\n";
exit($missing > 0 ? 1 : 0);
